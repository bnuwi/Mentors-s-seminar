#!/bin/bash

echo "Привет! Введите команду ('выход' для завершения):"

while true; do
    read -p "> " cmd

    case "$cmd" in
        Дата)
            echo "Текущая дата: $(date '+%Y-%m-%d')"
            ;;
        Время)
            echo "Текущее время: $(date '+%H:%M:%S')"
            ;;
        Привет)
            echo "Привет! Рад тебя видеть."
            ;;
        выход|Выход)
            echo "Выход из программы."
            break
            ;;
        *)
            echo "Неизвестная команда. Доступные команды: Дата, Время, Привет, выход"
            ;;
    esac
done
